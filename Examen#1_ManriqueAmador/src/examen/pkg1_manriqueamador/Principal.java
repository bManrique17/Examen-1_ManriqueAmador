    public String ordenarPalabra(String palabra) {
        char[] temp = new char[palabra.length()];
        String retorno = "";
        for (int i = 0; i < palabra.length(); i++) {
            temp[i] = palabra.charAt(i);
        }
        Arrays.sort(temp);
        for (int i = 0; i < palabra.length(); i++) {
            retorno += temp[i];
        }
        return retorno;
    }

